#include "virtualCounter.h"
#include <Arduino.h>
///////////////////////////
virtualCounter::virtualCounter()  // Constructor
{   
}
//__________________________________________________________________________________________________________________________________________________________________
void virtualCounter::init(void)
{
    count_Reg=0;
    compare_val=0; 
}
//__________________________________________________________________________________________________________________________________________________________________
 void virtualCounter::set(long val)
{
    compare_val=val;  
    count_Reg=0;
}
//__________________________________________________________________________________________________________________________________________________________________
bool virtualCounter::over(void)
{
   if(count_Reg>=compare_val)  
   {
      count_Reg=0;
      return true;
   }
   else
   {
      count_Reg++;
      return false;
   }
}
