#include "buzzer.h"
#include <Arduino.h>
///////////////////////////
buzzer::buzzer()  // Constructor
{   
}
//__________________________________________________________________________________________________________________________________________________________________
void buzzer::init(char pin)
{
    bzr_pin=pin;
    pinMode(pin, OUTPUT);
    digitalWrite(bzr_pin,HIGH); 
}
//__________________________________________________________________________________________________________________________________________________________________
 void buzzer::beep()
{
    digitalWrite(bzr_pin,LOW);
    delay(200);
    digitalWrite(bzr_pin,HIGH);   
    delay(500);
}
//__________________________________________________________________________________________________________________________________________________________________
void buzzer::beep(unsigned int val)
{
    digitalWrite(bzr_pin,LOW);
    delay(val);
    digitalWrite(bzr_pin,HIGH);
    delay(val);
}
//__________________________________________________________________________________________________________________________________________________________________
void buzzer::on()
{
    digitalWrite(bzr_pin,LOW);
}
//__________________________________________________________________________________________________________________________________________________________________         
void buzzer::off()
{
    digitalWrite(bzr_pin,HIGH);
}
